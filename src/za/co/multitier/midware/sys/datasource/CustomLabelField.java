public class CustomLabelField {

    private String field_name;
    private String field_description;
    private String field_value;
    private String field_position;
    private String field_type;

    public String getField_name() {
        return field_name;
    }

    public void setField_name(String field_name) {
        this.field_name = field_name;
    }

    public String getField_description() {
        return field_description;
    }

    public void setField_description(String field_description) {
        this.field_description = field_description;
    }

    public String getField_value() {
        return field_value;
    }

    public void setField_value(String field_value) {
        this.field_value = field_value;
    }

    public String getField_position() {
        return field_position;
    }

    public void setField_position(String field_position) {
        this.field_position = field_position;
    }

    public String getField_type() {
        return field_type;
    }

    public void setField_type(String field_type) {
        this.field_type = field_type;
    }


}
